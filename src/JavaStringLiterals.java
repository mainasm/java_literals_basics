public class JavaStringLiterals {
    public static void main(String[] args) {
        String myString = "I am a string literal";
        System.out.println("Who I'm I? \n" + myString);
        System.out.println("What is my length? ");

        //String method length() get length in characters
        System.out.println("I am " + myString.length()
                + " characters long");    }
}
